# Given three integers, determine which one is the largest.

# Input: Three integers (int).

# Output: Integer (int).

# Examples:

# assert max_of_three(1, 2, 3) == 3
# assert max_of_three(3, 2, 1) == 3
# assert max_of_three(1, 3, 2) == 3
# assert max_of_three(0, 0, 0) == 0

# How it’s used: this function can be used in various algorithms and computations where the highest
# value among multiple options needs to be selected.

# Precondition:

# -109 <= a, b, c <= 109.

################################################ SOLUTION#####################################################

def max_of_three(a: int, b: int, c: int) -> int:

    return max([a, b, c])


print("Example:")
print(max_of_three(1, 2, 3))
